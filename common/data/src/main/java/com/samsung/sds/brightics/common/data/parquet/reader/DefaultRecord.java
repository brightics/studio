package com.samsung.sds.brightics.common.data.parquet.reader;

public class DefaultRecord extends AbstractRecord<Object[]> {

    int currentIndex = 0;

    public DefaultRecord(int count) {
        this.values = new Object[count];
    }

    @Override
    public void add(int fieldIndex, Object value) {
        values[fieldIndex] = value;
        currentIndex = fieldIndex + 1;
    }

}
